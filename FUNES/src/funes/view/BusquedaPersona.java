    public void mostrarResultados(ArrayList<PersonaDenunciada> solicitudBuscarPersona) {

        resultadosBuscarPersona = solicitudBuscarPersona;
        listaResultados = new ArrayList<>();
        for (int entidad = 0; entidad < solicitudBuscarPersona.size(); entidad++) {
            //System.out.println(solicitudBuscarPersona.get(entidad).getSegundoApellido());
            String datos = solicitudBuscarPersona.get(entidad).getDatosGenerales();
            listaResultados.add(datos);
            System.out.println(datos);
        }

        String[] listaDatosAMostrar = listaResultados.toArray(new String[listaResultados.size()]);
        if (resultadosListaPanel != null) {
            resultadosListaPanel.dispose();
        }
        resultadosListaPanel = new JLisResultados(listaDatosAMostrar);
        resultadosListaPanel.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
    }

