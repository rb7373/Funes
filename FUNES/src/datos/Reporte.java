package datos;

public class Reporte {

    private String nicknameOrigen;
    private String razón;
    private int id;

    // Getters and Setters
    public String getNicknameOrigen() {
        return nicknameOrigen;
    }

    public void setNicknameOrigen(String nicknameOrigen) {
        this.nicknameOrigen = nicknameOrigen;
    }

    public String getRazón() {
        return razón;
    }

    public void setRazón(String razón) {
        this.razón = razón;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

}
